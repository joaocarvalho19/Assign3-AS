    private void lbStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lbStartActionPerformed
        String LBhost = LBHost.getText();
        int LBport = Integer.parseInt(LBPort.getText());
        String Monitorhost = MonitorHost.getText();
        int Monitorport = Integer.parseInt(MonitorPort.getText());
        
        System.out.println("Starting LB");
        this.server = new SServer(LBport);
        
        try {
            socketMonitor = new SClient(new Socket(Monitorhost, Monitorport));
            /*if(socketMonitor.createSocket())
                socketMonitor.writeObject(new Message("LB"));*/
        } catch (IOException ex) {
            System.err.println(ex);
        }
        
        this.lb = new LB(LBId, server, socketMonitor, this);
        this.lb.start();
        lbStart.setEnabled(false);
    }//GEN-LAST:event_lbStartActionPerformed

